namespace Test01
{
    class Calculator
    {
        public static int Add(int value1, int value2)
        {
            return value1 + value2;
        }
        public static int Sub(int value1, int value2)
        {
            return value1 - value2;
        }
        public static int Mult(int value1, int value2)
        {
            return value1 * value2;
        }
        public static int Div(int value1, int value2)
        {
            return value1 / value2;
        }
    }
}